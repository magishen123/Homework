t1 = int(input('Цена первого товара '))
t2 = int(input('Цена второго товара '))
t3 = int(input('Цена третьего товара '))
if t1 < t2 < t3:
    print('К оплате со скидкой- ', (t1 + t2 + t3) / 2)
elif t1 > t2 > t3:
    print('К оплате со скидкой- ', (t1 + t2 + t3) / 3)
else:
    print('К оплате:', t1 + t2 + t3)